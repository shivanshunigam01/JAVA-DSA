package GFG_Problems;

public class Right_Trianlge {
    public static void main(String[] args) {
        System.out.println("Let's print the right angle triangle");
        
        int n=5;
        for(int i =0;i<n;i++){
            for(int j=0;j<=i;j++){
                System.out.print("*");
            }
        
            System.out.println("");
        
        }
    }
}
